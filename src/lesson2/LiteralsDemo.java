public class LiteralsDemo {
    public static void main(String[] args) {
        boolean b = true;
        String a = "My first string in my project";
        char c = 'n';
        int i = 55;
        long lon = 220433L;
        int in = 07;
        int sixteen = 0X0005;
        float fl = 343434.454563F;
        double dl = 1.5e14;
        System.out.println("логический литерал " + b);
        System.out.println("строковый " + a);
        System.out.println("символьный " + c);
        System.out.println("целочисленный 2-й " + i);
        System.out.println("целочисленный 8-й " + lon);
        System.out.println("целочисленный 10-й " +in);
        System.out.println("целочисленный 16-й " +sixteen);
        System.out.println("литерал типа float " + fl);
        System.out.println("литерал типа double " + dl);
    }

}
